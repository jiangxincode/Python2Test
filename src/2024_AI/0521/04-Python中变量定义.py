'''
变量含义：
① 变量是存储数据的容器 => 只要有数据的地方，都有变量
② 变量中存放的数据是临时的 => 代码运行过程中有效的，代码运行结束 => 失效
③ 变量中存储的数据是可以发生变化的 => 变量中的值可以替换

基本语法：
变量名称 = 变量的值

注意：=不是日常生活中的等于含义，在Python代码中，它叫做赋值运算符，作用：把右边的值赋值给变量
'''
# 1. 定义一个变量name
name = '陈宇航'
# 2. 定义一个变量age
age = 23
# 3. 定义一个变量address
address = '北京市昌平区'

# 当我们有需要的时候，可以通过print()方法来访问变量
# 普及一个快捷键Ctrl + D
print(name)
print(age)
print(address)